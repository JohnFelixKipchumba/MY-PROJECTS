    private void toolSaveButtonHandler(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_toolSaveButtonHandler
        // confirm text fields not empty
        if(toolNameTF.getText().isBlank() || toolBuyQtyTF.getText().isBlank() || toolBuyDateTF.getText().isBlank()) {
            // display error message
            JOptionPane.showMessageDialog( null, "Please Fill All Fields!\nTool Description is optional!", "Add New Tool Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
        } else {
            try {                
                String toolSize = toolSizeComboBox.getSelectedItem().toString();
                int buyQty = Integer.parseInt(toolBuyQtyTF.getText());
                float buyPrice = Float.parseFloat(toolBuyPriceFormattedTF.getValue().toString());
                // validate purchased date input
                if(Authenticator.validateDate(toolBuyDateTF.getText()) == false) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input valid Date!\neg.2023-05-03", "Add New Tool Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
                }
                // check buy qty and price greater than zero
                else if(buyQty <= 0 || buyPrice <= 0.0) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Buy Quantity and Buy Price values should be greater than zero!", "Add New Tool Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
                }
                // record new tool details to database
                else {                    
                    database.addTool(toolNameTF.getText(), toolSize, buyPrice, toolBuyDateTF.getText(), buyQty, toolSaveButton);
                    database.updateToolDescription(toolDescriptionTextArea.getText(), toolNameTF.getText());
                }
                
            } catch(DateTimeParseException e) {
                // display error message
                JOptionPane.showMessageDialog( null, "Please input valid Date!", "Add New Tool Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
            }     
        } // end outer else
    }//GEN-LAST:event_toolSaveButtonHandler

