    private void borrowSubmitButtonHandler(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrowSubmitButtonHandler
        // confirm text fields not empty
        if(staffIdBorrowTF.getText().isBlank() || goodQtyBorrowTF.getText().isBlank() || damageQtyBorrowTF.getText().isBlank() || durationBorrowTF.getText().isBlank()) {
            // display error message
            JOptionPane.showMessageDialog( null, "Please Fill All Fields!", "Add Tool Borrow Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
        } else {
            int staffId = Integer.parseInt(staffIdBorrowTF.getText());
            int goodQty = Integer.parseInt(goodQtyBorrowTF.getText());
            int damagedQty = Integer.parseInt(damageQtyBorrowTF.getText());
            int duration = Integer.parseInt(durationBorrowTF.getText());
            // ensure duration greater than zero
            if(duration <= 0) {
                // display error message
                JOptionPane.showMessageDialog( null, "Please ensure duration greater than zero!", "Add Tool Borrow Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
            }
            // validate input id value
            else if(Authenticator.validateID(staffIdBorrowTF.getText()) == false) {
                // display error message
                JOptionPane.showMessageDialog( null, "Please input a valid ID number (8 digits)!", "Add Tool Borrow Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
            }
            // confirm selection from table
            else if(borrowTable.getSelectionModel().isSelectionEmpty() == true) {
                // displaying of NULL items selected error message */
                JOptionPane.showMessageDialog( null, "Select a Tool from Table!","Tool Borrow Error- Street Sparkle", JOptionPane.WARNING_MESSAGE ); 
            }
            // record borrow into database
            else {
                aTool.addToolBorrow(staffId, goodQty, damagedQty, duration, borrowTable, borrowSubmitButton);
            }
        }
    }//GEN-LAST:event_borrowSubmitButtonHandler

