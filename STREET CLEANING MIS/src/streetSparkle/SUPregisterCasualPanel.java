    private void saveCasualButtonHandler(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveCasualButtonHandler
        // confirm text fields not empty
        if(fNameTF.getText().isBlank() || midNameTF.getText().isBlank() || surNameTF.getText().isBlank() || nationalIDTF.getText().isBlank() || birthDateTF.getText().isBlank() || 
                wageFormattedTF.getText().isBlank() || phoneTF.getText().isBlank() || emailTF.getText().isBlank() || addressTF.getText().isBlank()) {
            // display error message
            JOptionPane.showMessageDialog( null, "Please Fill All Fields!", "Add New Casual Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
        } else {
            try {                
                String casualGender = genderComboBox.getSelectedItem().toString();
                String casualCounty = countyComboBox.getSelectedItem().toString();
                float wage = Float.parseFloat(wageFormattedTF.getValue().toString());
                // validate input id value
                if(Authenticator.validateID(nationalIDTF.getText()) == false) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input a valid ID number (8 digits)!", "Add New Casual Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
                }
                // validate date of birth input
                else if(Authenticator.validateDate(birthDateTF.getText()) == false) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input valid Date!\neg.2023-05-03", "Add New Casual Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
                }
                // validate mobile phone no. input
                else if(Authenticator.validateMobileNo(phoneTF.getText()) == false) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input a valid Mobile Phone number (10 digits)!", "Add New Casual Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
                }
                // validate email address input
                else if(Authenticator.validateEmail(emailTF.getText()) == false) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input a valid Email address!", "Add New Casual Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
                }
                // record new casual details to database
                else { 
                    String staffSN = Authenticator.getStaffSN();
                    aCasual.setCasualId(); // set the casual SN
                    String casualId = aCasual.getCasualId(); // get the set SN
                    database.addCasual(casualId, fNameTF.getText(), midNameTF.getText(), surNameTF.getText(), casualGender, Integer.parseInt(nationalIDTF.getText()), 
                            birthDateTF.getText(), Integer.parseInt(phoneTF.getText()), emailTF.getText(), addressTF.getText(), casualCounty, staffSN, wage, saveCasualButton);
                    casualIDTF.setText(casualId);
                }              
            } catch(DateTimeParseException e) {
                // display error message
                JOptionPane.showMessageDialog( null, "Please input valid Date!", "Add New Casual Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
            }     
        } // end outer else
    }//GEN-LAST:event_saveCasualButtonHandler

