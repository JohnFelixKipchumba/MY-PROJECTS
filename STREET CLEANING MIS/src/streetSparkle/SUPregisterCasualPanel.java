    private void updateCasualButtonHandler(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateCasualButtonHandler
        // confirm if casual id no. in database
        boolean hasId;
        hasId = database.confirmCasualId(Integer.parseInt(idUpdateTF.getText()));
        
        // update phone no.
        if(phoneUpdateTF.isEnabled() && phoneUpdateTF.getText().isBlank() == false && hasId == true) {
            // validate mobile phone no. input
            if(Authenticator.validateMobileNo(phoneUpdateTF.getText()) == false) {
                // display error message
                JOptionPane.showMessageDialog( null, "Please input a valid Mobile Phone number (10 digits)!", "Update Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
            } else {
                // record update to db
                database.updateCasualPhone(Integer.parseInt(phoneUpdateTF.getText()), Integer.parseInt(idUpdateTF.getText())); 
                phoneLabel.setText("Updated Successfully");
            }
        } else {
            phoneLabel.setText("Required field");
        }  
        
        // update address
        if(addressUpdateTF.isEnabled() && addressUpdateTF.getText().isBlank() == false && hasId == true) {
            // record update to db
            database.updateCasualAddress(addressUpdateTF.getText(), Integer.parseInt(idUpdateTF.getText())); 
            addressLabel.setText("Updated Successfully");
        } else {
            addressLabel.setText("Required field");
        } 
        
        // update county
        if(countyUpdateComboBox.isEnabled() && hasId == true) {
            // record update to db
            database.updateCasualCounty(countyUpdateComboBox.getSelectedItem().toString(), Integer.parseInt(idUpdateTF.getText())); 
            countyLabel.setText("Updated Successfully");
        } else {
            countyLabel.setText("Required field");
        } 
        
        // update wage
        if(wageUpdateFormattedTF.isEnabled() && wageUpdateFormattedTF.getText().isBlank() == false && hasId == true) {
            // record update to db
            database.updateCasualWage(Float.parseFloat(wageUpdateFormattedTF.getValue().toString()), Integer.parseInt(idUpdateTF.getText())); 
            wageLabel.setText("Updated Successfully");
        } else {
            wageLabel.setText("Required field");
        } 
    }//GEN-LAST:event_updateCasualButtonHandler

