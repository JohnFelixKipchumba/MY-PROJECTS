    private void compSubmitButtonHandler(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_compSubmitButtonHandler
        // confirm text fields not empty
        if(compTitleTF.getText().isBlank() || compPhoneTF.getText().isBlank() || compEmailTF.getText().isBlank() || compAddressTF.getText().isBlank()) {
            // display error message
            JOptionPane.showMessageDialog( null, "Please Fill All Fields!", "Add New Client Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
        } else {
            String clientCounty = compCountyComboBox.getSelectedItem().toString();
            // validate mobile phone no. input
            if(Authenticator.validateMobileNo(compPhoneTF.getText()) == false) {
                // display error message
                JOptionPane.showMessageDialog( null, "Please input a valid Mobile Phone number (10 digits)!", "Add New Client Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
            }
            // validate email address input
            else if(Authenticator.validateEmail(compEmailTF.getText()) == false) {
                // display error message
                JOptionPane.showMessageDialog( null, "Please input a valid Email address!", "Add New Client Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
            }
            // record new enterprise client details to database
            else {                    
                database.addClient("", "", "", Integer.parseInt(compPhoneTF.getText()), compEmailTF.getText(),
                        compAddressTF.getText(), clientCounty, compTitleTF.getText(), compSubmitButton);
            }
        }
    }//GEN-LAST:event_compSubmitButtonHandler

