    private void submitSupplierButtonHandler(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitSupplierButtonHandler
        // confirm text fields not empty
        if(titleTF.getText().isBlank() || phoneTF.getText().isBlank() || emailTF.getText().isBlank() || addressTF.getText().isBlank() || terminationTF.getText().isBlank()) {
            // display error message
            JOptionPane.showMessageDialog( null, "Please Fill All Fields!", "Add New Supplier Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
        } else {
            try {                
                String supplierCounty = countyComboBox.getSelectedItem().toString();
                // validate termination date input
                if(Authenticator.validateDate(terminationTF.getText()) == false) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input valid Date!\neg.2023-05-03", "Add New Supplier Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
                }
                // validate mobile phone no. input
                else if(Authenticator.validateMobileNo(phoneTF.getText()) == false) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input a valid Mobile Phone number (10 digits)!", "Add New Supplier Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
                }
                // validate email address input
                else if(Authenticator.validateEmail(emailTF.getText()) == false) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input a valid Email address!", "Add New Supplier Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
                }
                // check products selected
                else if(productsList.isSelectionEmpty() == true) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please Select supplier products!", "Add New Supplier Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
                }
                // record new supplier details to database
                else {                    
                    String products = productsList.getSelectedValuesList().toString(); // get selected products
                    database.addSupplier(titleTF.getText(), Integer.parseInt(phoneTF.getText()), emailTF.getText(), addressTF.getText(),
                            supplierCounty, products, terminationTF.getText(), submitSupplierButton);
                }
                
            } catch(DateTimeParseException e) {
                // display error message
                JOptionPane.showMessageDialog( null, "Please input valid Date!", "Add New Supplier Error - Street Sparkle", JOptionPane.WARNING_MESSAGE );
            }     
        } // end outer else
    }//GEN-LAST:event_submitSupplierButtonHandler


