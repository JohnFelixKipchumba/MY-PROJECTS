    private void addStockJBHandler(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStockJBHandler
        // if text fields not empty, confirm drug then add stock record
        if(itemNameTF.getText().isBlank() || measurementFormattedTF.getText().isBlank() || batchTF.getText().isBlank() ||
                expiryTF.getText().isBlank() || quantityTF.getText().isBlank() || buyPriceFormattedTF.getText().isBlank() ||
                sellPriceFormattedTF.getText().isBlank() || taxFormattedTF.getText().isBlank() || contractorIDTF.getText().isBlank() ) {
            // display error message
            JOptionPane.showMessageDialog( null, "Please Fill All Fields!", "Fill Stock Error - MyDuka", JOptionPane.WARNING_MESSAGE );           
        } else {
            try {
                String measureValue = measurementFormattedTF.getValue().toString();
                String measure = measurementCB.getSelectedItem().toString();
                float buyPrice = Float.parseFloat(buyPriceFormattedTF.getValue().toString());
                float sellPrice = Float.parseFloat(sellPriceFormattedTF.getValue().toString());
                float taxValue = Float.parseFloat(taxFormattedTF.getValue().toString());
                // validate input of quantity
                if(Integer.parseInt(quantityTF.getText()) <= 0) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input valid quantity greater than zero!", "Fill Stock Error - MyDuka", JOptionPane.WARNING_MESSAGE );
                }
                // validate tax input value
                if(taxValue < 0.00 || taxValue > 99.00) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input valid tax from 0.00 - 99.00 !", "Fill Stock Error - MyDuka", JOptionPane.WARNING_MESSAGE );
                }
                // validate input of expiry date
                else if(Authenticator.validateDate(expiryTF.getText()) == false || LocalDate.parse(expiryTF.getText()).isBefore(LocalDate.now())) {
                    // display error message
                    JOptionPane.showMessageDialog( null, "Please input valid Date!\neg.2022-05-03\nDate should be after today!", "Fill Stock Error - MyDuka", JOptionPane.WARNING_MESSAGE );
                } 
                // record stock changes to database
                else {                
                    String itemMeasure = Authenticator.concatenate(measureValue, measure);
                    database.fillStock(itemNameTF.getText(), itemMeasure, contractorIDTF.getText(), buyPrice, sellPrice, 
                                        batchTF.getText(), Integer.parseInt(quantityTF.getText()),  expiryTF.getText(), taxValue);                     
                }
            } catch(DateTimeParseException e) {
                // display error message
                JOptionPane.showMessageDialog( null, "Please input valid Date!", "Fill Stock Error - MyDuka", JOptionPane.ERROR_MESSAGE );
            }    
        } // end outer else
    }//GEN-LAST:event_addStockJBHandler

